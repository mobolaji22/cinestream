// Helper function to get content by ID
export function getContentById(id: string) {
  // Check if it's a movie
  if (id === movieDetails.id) return movieDetails
  if (id === seriesDetails.id) return seriesDetails

  // Check in all movie arrays
  const allMovies = [...recommendedMovies, ...trendingMovies, ...newReleases]
  const foundMovie = allMovies.find((movie) => movie.id === id)
  if (foundMovie) {
    // Return a more complete movie object with defaults for missing properties
    return {
      ...foundMovie,
      type: "movie",
      backdropUrl: foundMovie.backdropUrl || "/placeholder.svg?height=800&width=1600",
      description:
        foundMovie.description || `${foundMovie.title} is a ${foundMovie.year} ${foundMovie.genres.join(", ")} movie.`,
      duration: foundMovie.duration || "1h 30m",
      director: "Unknown Director",
      cast: [],
      similarMovies: [],
    }
  }

  // Check in all series arrays
  const allSeries = [...recommendedSeries, ...trendingSeries]
  const foundSeries = allSeries.find((series) => series.id === id)
  if (foundSeries) {
    // Return a more complete series object with defaults for missing properties
    return {
      ...foundSeries,
      type: "series",
      backdropUrl: foundSeries.backdropUrl || "/placeholder.svg?height=800&width=1600",
      description:
        foundSeries.description ||
        `${foundSeries.title} is a ${foundSeries.year} ${foundSeries.genres.join(", ")} series.`,
      creators: ["Unknown Creator"],
      cast: [],
      seasons: [],
      similarSeries: [],
    }
  }

  // Return default if not found
  return null
}

// Helper function to get all content
export function getAllContent() {
  const movies = [...recommendedMovies, ...trendingMovies, ...newReleases]
  const series = [...recommendedSeries, ...trendingSeries]

  // Remove duplicates
  const uniqueMovies = movies.filter((movie, index, self) => index === self.findIndex((m) => m.id === movie.id))

  const uniqueSeries = series.filter((series, index, self) => index === self.findIndex((s) => s.id === series.id))

  return [...uniqueMovies, ...uniqueSeries]
}

